    def clear_screen(self):
        os.system("cls" if os.name == "nt" else "clear")

    def print_separator(self, char="═", width=50):
        print(char * width)

    def print_title(self, title):
        print()
        self.print_separator("━")
        print(f"  {title}")
        self.print_separator("━")
        print()

    def display_node(self):
        self.clear_screen()
        node = NODES[self.current_node]
        self.print_title(node["title"])
        # 用 typing effect 分段打印正文
        for line in node["text"].strip().split("\n"):
            print(f"  {line}")
        print()

        if not node["choices"]:
            # 结局节点
            self.print_separator()
            print()
            print("  请输入:")
            print("    1. 重新开始")
            print("    2. 返回主菜单")
            print("    3. 退出游戏")
            print()
            return True  # is_ending
        else:
            for i, choice in enumerate(node["choices"], 1):
                print(f"  [{i}] {choice['text']}")
            print()
            print("  输入 1 或 2 做出选择，输入 s 保存进度")
            print()
            return False  # not ending

    def make_choice(self, choice_idx):
        node = NODES[self.current_node]
        if 0 <= choice_idx < len(node["choices"]):
            self.path_history.append(self.current_node)
            self.current_node = node["choices"][choice_idx]["next"]

    def save_game(self):
        if not os.path.exists(SAVE_DIR):
            os.makedirs(SAVE_DIR)

        save_data = {
            "player_name": self.player_name,
            "current_node": self.current_node,
            "path_history": self.path_history,
            "title": NODES[self.current_node]["title"],
            "saved_at": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        }

        filename = f"{SAVE_DIR}/save_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
        with open(filename, "w", encoding="utf-8") as f:
            json.dump(save_data, f, ensure_ascii=False, indent=2)

        print(f"\n  ✓ 存档已保存至: {filename}")
        input("\n  按回车键继续...")

    def load_game(self, filename):
        with open(filename, "r", encoding="utf-8") as f:
            data = json.load(f)
        self.player_name = data["player_name"]
        self.current_node = data["current_node"]
        self.path_history = data["path_history"]
        return data

    @staticmethod
    def list_saves():
        if not os.path.exists(SAVE_DIR):
            return []
        saves = []
        for f in sorted(os.listdir(SAVE_DIR), reverse=True):
            if f.endswith(".json"):
                filepath = os.path.join(SAVE_DIR, f)
                try:
                    with open(filepath, "r", encoding="utf-8") as fp:
                        data = json.load(fp)
                    saves.append({
                        "filename": filepath,
                        "name": data.get("player_name", "未知"),
                        "title": data.get("title", "未知"),
                        "saved_at": data.get("saved_at", "未知"),
                    })
                except (json.JSONDecodeError, KeyError):
                    pass
        return saves


