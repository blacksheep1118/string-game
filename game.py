def load_menu(game):
    """存档选择菜单，返回 True 表示成功加载"""
    saves = Game.list_saves()
    if not saves:
        print("\n  没有找到存档文件。")
        input("\n  按回车键返回...")
        return False

    while True:
        game.clear_screen()
        print()
        print("╔══════════════════════════════════════╗")
        print("║           📂 读 取 存 档              ║")
        print("╠══════════════════════════════════════╣")
        for i, save in enumerate(saves, 1):
            print(f"║  {i}. {save['name']} — {save['title']}")
            print(f"║     {save['saved_at']}")
        print("║                                      ║")
        print(f"║  {len(saves) + 1}. 返回主菜单               ║")
        print("╚══════════════════════════════════════╝")
        print()

        choice = input("  请选择存档: ").strip()
        try:
            idx = int(choice)
            if 1 <= idx <= len(saves):
                data = game.load_game(saves[idx - 1]["filename"])
                print(f"\n  ✓ 已加载存档: {saves[idx - 1]['saved_at']}")
                print(f"  角色: {data['player_name']}")
                print(f"  进度: {data['title']}")
                input("\n  按回车键继续...")
                return True
            elif idx == len(saves) + 1:
                return False
        except ValueError:
            pass
        print("  输入无效，请重试。")


def handle_ending(game):
    """处理结局后的输入"""
    while True:
        choice = input("  请选择: ").strip()
        if choice == "1":
            game.current_node = "start"
            game.path_history = []
            return "restart"
        elif choice == "2":
            return "menu"
        elif choice == "3":
            return "quit"
        else:
            print("  请输入 1、2 或 3")


def run_game(game):
    """主游戏循环"""
    while True:
        is_ending = game.display_node()

        if is_ending:
            action = handle_ending(game)
            if action == "restart":
                continue
            elif action == "menu":
                return "menu"
            else:
                return "quit"

        user_input = input("  你的选择: ").strip().lower()

        if user_input == "s":
            game.save_game()
            continue
        elif user_input == "1":
            game.make_choice(0)
        elif user_input == "2":
            node = NODES[game.current_node]
            if len(node["choices"]) >= 2:
                game.make_choice(1)
            else:
                print("  无效选择，请重新输入。")
                input("  按回车键继续...")
        else:
            print("  请输入 1、2 或 s（存档）。")
            input("  按回车键继续...")


